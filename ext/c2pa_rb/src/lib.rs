use magnus::{Error, Ruby};

#[magnus::init]
fn init(_ruby: &Ruby) -> Result<(), Error> {
    Ok(())
}

pub fn add(left: usize, right: usize) -> usize {
    left + right
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn it_works() {
        let result = add(2, 2);
        assert_eq!(result, 4);
    }
}
